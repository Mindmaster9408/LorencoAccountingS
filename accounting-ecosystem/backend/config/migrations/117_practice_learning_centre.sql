CREATE TABLE IF NOT EXISTS practice_cpd_records (
    id                     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id              INTEGER NOT NULL,
    team_member_id           INTEGER NOT NULL,   -- plain integer, no FK — practice_team_members

    provider                  TEXT,
    course_name                TEXT NOT NULL,
    hours                        NUMERIC NOT NULL DEFAULT 0,
    category                     TEXT,           -- free text — CPD categories are typically defined by an external professional body (SAICA/SAIT/etc.), a different taxonomy from internal Skills Matrix categories, so deliberately not linked to practice_skill_categories

    certificate_number             TEXT,
    issue_date                      DATE,
    expiry_date                      DATE,
    evidence                          TEXT,        -- free-text reference/URL — no file upload system exists in this codebase

    status                             TEXT NOT NULL DEFAULT 'recorded' CHECK (status IN ('recorded', 'verified', 'expired')),
    is_active                           BOOLEAN NOT NULL DEFAULT true,   -- soft-delete flag, independent of `status` (same reasoning as practice_team_certifications.is_active in Codebox 59)

    notes                                 TEXT,
    metadata                               JSONB NOT NULL DEFAULT '{}',

    created_by                             INTEGER,
    created_at                              TIMESTAMPTZ DEFAULT NOW(),
    updated_at                              TIMESTAMPTZ DEFAULT NOW()
);

