-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_learning_progress
-- Point-in-time progress snapshots — a history/trend record, distinct from
-- practice_learning_plans.overall_progress (which always holds only the
-- latest computed value). Written whenever a manager explicitly captures a
-- progress snapshot, and whenever a plan is marked completed/cancelled.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_learning_progress (
    id                     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id              INTEGER NOT NULL,
    learning_plan_id         INTEGER NOT NULL,   -- plain integer, no FK — practice_learning_plans

    snapshot_date              DATE NOT NULL DEFAULT CURRENT_DATE,
    overall_progress             NUMERIC NOT NULL CHECK (overall_progress BETWEEN 0 AND 100),
    goals_total                   INTEGER NOT NULL DEFAULT 0,
    goals_completed                INTEGER NOT NULL DEFAULT 0,
    hours_planned                   NUMERIC NOT NULL DEFAULT 0,
    hours_completed                  NUMERIC NOT NULL DEFAULT 0,

    notes                             TEXT,
    metadata                           JSONB NOT NULL DEFAULT '{}',

    created_by                         INTEGER,
    created_at                          TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_plpr_company_id
    ON practice_learning_progress (company_id);

CREATE INDEX IF NOT EXISTS idx_plpr_plan_id
    ON practice_learning_progress (learning_plan_id, snapshot_date DESC);

COMMENT ON TABLE practice_learning_progress IS
    'Codebox 60 — Point-in-time progress snapshots for a learning plan (trend history). Distinct from practice_learning_plans.overall_progress, which always holds only the current computed value.';

