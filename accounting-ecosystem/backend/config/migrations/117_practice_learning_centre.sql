-- Migration 117: Practice Learning, Development & Training Centre
-- Codebox 60 — Lorenco Practice Management
--
-- NOT AI coaching. NOT automatic development plans. NOT an LMS. NOT
-- external learning provider integration. NOT employee performance
-- reviews. Complements the Skills Matrix (Codebox 59) — fully manager
-- controlled, deterministic, explainable.
--
-- Tables created (all IF NOT EXISTS — safe to re-run):
--   practice_learning_plans
--   practice_learning_goals
--   practice_learning_activities
--   practice_learning_progress
--   practice_cpd_records
--   practice_learning_events

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_learning_plans
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_learning_plans (
    id                        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id                INTEGER NOT NULL,
    team_member_id            INTEGER NOT NULL,   -- plain integer, no FK — practice_team_members (Codebox 41 convention)

    plan_name                 TEXT NOT NULL,
    description                TEXT,

    start_date                 DATE,
    target_completion_date      DATE,

    status                      TEXT NOT NULL DEFAULT 'draft' CHECK (status IN ('draft', 'active', 'on_hold', 'completed', 'cancelled')),

    mentor_team_member_id       INTEGER,   -- plain integer, no FK — practice_team_members. Nullable — not every plan has a formal mentor.

    -- Cached snapshot of the live progress calculation — recomputed and
    -- rewritten by calculateLearningProgress() on every goal/activity write
    -- that touches this plan, never edited directly. See learning-centre.js.
    overall_progress             NUMERIC NOT NULL DEFAULT 0 CHECK (overall_progress BETWEEN 0 AND 100),

    notes                        TEXT,
    metadata                     JSONB NOT NULL DEFAULT '{}',

    created_by                   INTEGER,   -- plain integer, no FK — users.id
    created_at                    TIMESTAMPTZ DEFAULT NOW(),
    updated_at                    TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_plp_company_id
    ON practice_learning_plans (company_id);

CREATE INDEX IF NOT EXISTS idx_plp_team_member_id
    ON practice_learning_plans (team_member_id);

CREATE INDEX IF NOT EXISTS idx_plp_mentor_id
    ON practice_learning_plans (mentor_team_member_id)
    WHERE mentor_team_member_id IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_plp_status
    ON practice_learning_plans (company_id, status);

CREATE OR REPLACE FUNCTION fn_plp_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_plp_updated_at ON practice_learning_plans;
CREATE TRIGGER tg_plp_updated_at
    BEFORE UPDATE ON practice_learning_plans
    FOR EACH ROW EXECUTE FUNCTION fn_plp_updated_at();

COMMENT ON TABLE practice_learning_plans IS
    'Codebox 60 — Manager-created development plans for a team member, optionally with a mentor. Fully manager controlled — no self-service editing, no automatic plan creation.';

