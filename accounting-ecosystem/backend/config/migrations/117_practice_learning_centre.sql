-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_learning_events  (append-only audit log)
-- Never updated. Never deleted.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_learning_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,

    event_type            TEXT NOT NULL CHECK (event_type IN (
                              'plan_created', 'plan_updated', 'plan_archived',
                              'goal_created', 'goal_updated', 'goal_completed', 'goal_archived',
                              'activity_created', 'activity_updated', 'activity_completed', 'activity_archived',
                              'cpd_recorded', 'cpd_updated', 'cpd_archived',
                              'progress_snapshot_created'
                          )),
    entity_type              TEXT,    -- 'plan' | 'goal' | 'activity' | 'cpd_record' | 'progress_snapshot'
    entity_id                 INTEGER,  -- plain integer, no FK (Codebox 41 convention)

    actor_user_id             INTEGER,
    notes                      TEXT,
    metadata                    JSONB NOT NULL DEFAULT '{}',
    created_at                   TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ple_company_id
    ON practice_learning_events (company_id);

CREATE INDEX IF NOT EXISTS idx_ple_entity
    ON practice_learning_events (entity_type, entity_id)
    WHERE entity_id IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_ple_created_desc
    ON practice_learning_events (created_at DESC);

COMMENT ON TABLE practice_learning_events IS
    'Codebox 60 — Append-only log of Learning Centre changes. Never updated or deleted.';
