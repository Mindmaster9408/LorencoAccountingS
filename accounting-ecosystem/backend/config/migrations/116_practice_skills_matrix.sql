-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_team_certifications
-- One row per (team member, certification) actually held.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_team_certifications (
    id                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id            INTEGER NOT NULL,
    team_member_id        INTEGER NOT NULL,   -- plain integer, no FK — practice_team_members
    certification_id      INTEGER NOT NULL,   -- plain integer, no FK — practice_certifications

    issue_date             DATE,
    expiry_date             DATE,
    status                  TEXT NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'expired', 'pending', 'revoked')),
    certificate_number       TEXT,
    notes                    TEXT,
    is_active                BOOLEAN NOT NULL DEFAULT true,   -- distinct from `status`: this is the soft-delete/archive flag for a mistaken or duplicate entry, independent of the certification's own real-world lifecycle (active/expired/pending/revoked)

    metadata                  JSONB NOT NULL DEFAULT '{}',

    created_at                TIMESTAMPTZ DEFAULT NOW(),
    updated_at                TIMESTAMPTZ DEFAULT NOW(),
    created_by                INTEGER,
    updated_by                INTEGER
);

CREATE INDEX IF NOT EXISTS idx_ptc_company_id
    ON practice_team_certifications (company_id);

CREATE INDEX IF NOT EXISTS idx_ptc_team_member_id
    ON practice_team_certifications (team_member_id);

CREATE INDEX IF NOT EXISTS idx_ptc_certification_id
    ON practice_team_certifications (certification_id);

CREATE INDEX IF NOT EXISTS idx_ptc_expiry_date
    ON practice_team_certifications (company_id, expiry_date)
    WHERE expiry_date IS NOT NULL;

CREATE OR REPLACE FUNCTION fn_ptc_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_ptc_updated_at ON practice_team_certifications;
CREATE TRIGGER tg_ptc_updated_at
    BEFORE UPDATE ON practice_team_certifications
    FOR EACH ROW EXECUTE FUNCTION fn_ptc_updated_at();

COMMENT ON TABLE practice_team_certifications IS
    'Codebox 59 — Certifications actually held by a team member. expiry_date indexed to support future expiry-reminder codeboxes (not built here — see spec "Allow expiry reminders later").';

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_skill_events  (append-only audit log)
-- Never updated. Never deleted.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_skill_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         INTEGER NOT NULL,

    event_type         TEXT NOT NULL CHECK (event_type IN (
                           'category_created', 'category_updated', 'category_archived',
                           'skill_created', 'skill_updated', 'skill_archived',
                           'team_skill_updated', 'team_skill_archived',
                           'certification_created', 'certification_updated', 'certification_archived',
                           'team_certification_added', 'team_certification_updated', 'team_certification_archived',
                           'skills_seeded'
                       )),
    entity_type         TEXT,        -- 'category' | 'skill' | 'team_skill' | 'certification' | 'team_certification'
    entity_id            INTEGER,    -- plain integer, no FK (Codebox 41 convention)

    actor_user_id       INTEGER,
    notes                TEXT,
    metadata              JSONB NOT NULL DEFAULT '{}',
    created_at             TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pse_company_id
    ON practice_skill_events (company_id);

CREATE INDEX IF NOT EXISTS idx_pse_entity
    ON practice_skill_events (entity_type, entity_id)
    WHERE entity_id IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_pse_created_desc
    ON practice_skill_events (created_at DESC);

COMMENT ON TABLE practice_skill_events IS
    'Codebox 59 — Append-only log of Skills Matrix changes. Never updated or deleted.';
