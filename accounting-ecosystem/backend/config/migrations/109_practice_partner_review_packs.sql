CREATE INDEX IF NOT EXISTS idx_pprpe_company_pack
    ON practice_partner_review_pack_events (company_id, pack_id);

CREATE INDEX IF NOT EXISTS idx_pprpe_created_desc
    ON practice_partner_review_pack_events (created_at DESC);

COMMENT ON TABLE practice_partner_review_pack_events IS
    'Codebox 52 — Append-only audit log for partner review pack lifecycle events (generated, updated, submitted, approved, rejected, cancelled, report viewed, PDF downloaded). Never updated or deleted.';
