-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_secretarial_events  (append-only audit log + Timeline source)
-- Never updated. Never deleted. GET /timeline reads directly from this table.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_secretarial_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,
    client_id             INTEGER NOT NULL,   -- plain integer, no FK — practice_clients (every event is client-scoped, unlike other modules' events tables)

    event_type              TEXT NOT NULL CHECK (event_type IN (
                                'profile_created', 'profile_updated',
                                'director_appointed', 'director_resigned', 'director_updated',
                                'shareholder_added', 'shareholder_updated', 'share_transferred',
                                'annual_return_created', 'annual_return_submitted', 'annual_return_updated',
                                'company_detail_changed', 'manager_note'
                             )),
    entity_type                TEXT,    -- 'profile' | 'director' | 'shareholder' | 'annual_return'
    entity_id                    INTEGER,  -- plain integer, no FK (Codebox 41 convention)

    actor_user_id                  INTEGER,
    notes                            TEXT,
    metadata                          JSONB NOT NULL DEFAULT '{}',
    created_at                         TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pse_company_id
    ON practice_secretarial_events (company_id);

CREATE INDEX IF NOT EXISTS idx_pse_client_id
    ON practice_secretarial_events (client_id, created_at DESC);

COMMENT ON TABLE practice_secretarial_events IS
    'Codebox 62 — Append-only log of Secretarial changes per client. Never updated or deleted. Doubles as the Timeline data source (GET /timeline reads this directly — no separate timeline table).';
