CREATE INDEX IF NOT EXISTS idx_pre_risk_id
    ON practice_risk_events (risk_id);

CREATE INDEX IF NOT EXISTS idx_pre_company_risk
    ON practice_risk_events (company_id, risk_id);

CREATE INDEX IF NOT EXISTS idx_pre_created_desc
    ON practice_risk_events (created_at DESC);

