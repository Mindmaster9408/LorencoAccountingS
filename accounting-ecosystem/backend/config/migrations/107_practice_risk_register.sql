-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_risk_reviews
-- Review history — periodic re-assessment of a risk. Two-step: create (draft,
-- scheduled) then complete (assessment snapshot recorded).
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_risk_reviews (
    id                        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id                INTEGER NOT NULL,
    risk_id                   INTEGER NOT NULL,       -- plain integer, no FK (Codebox 41 convention)

    review_status             TEXT NOT NULL DEFAULT 'draft' CHECK (review_status IN (
                                  'draft', 'completed', 'cancelled'
                              )),

    -- Snapshot of the assessment AT the time this review was completed —
    -- never modified after completion, so history is preserved even if the
    -- parent risk's current likelihood/impact/residual_risk change later.
    likelihood_at_review      INTEGER CHECK (likelihood_at_review IS NULL OR likelihood_at_review BETWEEN 1 AND 5),
    impact_at_review          INTEGER CHECK (impact_at_review     IS NULL OR impact_at_review     BETWEEN 1 AND 5),
    residual_risk_at_review   INTEGER CHECK (residual_risk_at_review IS NULL OR residual_risk_at_review BETWEEN 1 AND 25),

    review_notes              TEXT,
    next_review_date          DATE,

    reviewed_at               TIMESTAMPTZ,
    reviewed_by               INTEGER,

    created_at                TIMESTAMPTZ DEFAULT NOW(),
    updated_at                TIMESTAMPTZ DEFAULT NOW(),
    created_by                INTEGER
);

CREATE INDEX IF NOT EXISTS idx_prr_risk_id
    ON practice_risk_reviews (risk_id);

CREATE INDEX IF NOT EXISTS idx_prr_company_risk
    ON practice_risk_reviews (company_id, risk_id);

CREATE INDEX IF NOT EXISTS idx_prr_status
    ON practice_risk_reviews (company_id, review_status);

-- Auto-update updated_at
CREATE OR REPLACE FUNCTION fn_prr_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_prr_updated_at ON practice_risk_reviews;
CREATE TRIGGER tg_prr_updated_at
    BEFORE UPDATE ON practice_risk_reviews
    FOR EACH ROW EXECUTE FUNCTION fn_prr_updated_at();

COMMENT ON TABLE practice_risk_reviews IS
    'Codebox 49 — Risk review history. Each row is a periodic re-assessment; snapshot fields are frozen once review_status = completed.';

-- ─────────────────────────────────────────────────────────────────────────────
