    completed                        BOOLEAN NOT NULL DEFAULT false,
    completed_at                       TIMESTAMPTZ,
    completed_by                         INTEGER,

    notes                                   TEXT,
    sort_order                               INTEGER NOT NULL DEFAULT 0,

    created_at                                 TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                   TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_poc_company_id  ON practice_onboarding_checklists (company_id);
CREATE INDEX IF NOT EXISTS idx_poc_profile_id    ON practice_onboarding_checklists (profile_id, sort_order);

CREATE OR REPLACE FUNCTION fn_poc_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_poc_updated_at ON practice_onboarding_checklists;
CREATE TRIGGER tg_poc_updated_at
    BEFORE UPDATE ON practice_onboarding_checklists
    FOR EACH ROW EXECUTE FUNCTION fn_poc_updated_at();

COMMENT ON TABLE practice_onboarding_checklists IS
    'Codebox 70 — Entity-type-specific onboarding checklist (one row per item). Generated from a deterministic per-entity-type template (see ONBOARDING_CHECKLIST_DEFAULTS in client-onboarding.js) or added manually. Manager may regenerate — regeneration only adds missing items, never removes or resets existing ones.';

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_onboarding_events  (append-only audit log)
-- Never updated. Never deleted. No status change and no silent
-- initialization without a matching event — enforced at the application
-- layer in client-onboarding.js.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_onboarding_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,
    client_id             INTEGER,   -- plain integer, no FK — practice_clients (denormalized for fast per-client history queries)

    source_type             TEXT NOT NULL CHECK (source_type IN ('profile', 'step', 'checklist_item')),
    source_id                 INTEGER NOT NULL,   -- plain integer, no FK (Codebox 41 convention)

    event_type                  TEXT NOT NULL CHECK (event_type IN (
                                    'profile_created', 'step_created', 'step_completed', 'checklist_generated',
                                    'status_changed', 'review_completed', 'onboarding_completed'
                                )),
    old_status                    TEXT,
    new_status                      TEXT,

    actor_user_id                      INTEGER,
    notes                                 TEXT,
    metadata                              JSONB NOT NULL DEFAULT '{}',
    created_at                              TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_poe_company_id  ON practice_onboarding_events (company_id);
CREATE INDEX IF NOT EXISTS idx_poe_source       ON practice_onboarding_events (source_type, source_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_poe_client_id    ON practice_onboarding_events (client_id) WHERE client_id IS NOT NULL;

COMMENT ON TABLE practice_onboarding_events IS
    'Codebox 70 — Append-only log of onboarding profile/step/checklist activity, including every module buildOnboardingWorkspace() initializes. Never updated or deleted. No silent initialization — every create-if-missing action writes an event.';
