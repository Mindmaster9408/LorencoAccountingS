                                  'directors_meeting', 'shareholders_meeting', 'annual_general_meeting',
                                  'special_general_meeting', 'trustees_meeting', 'members_meeting', 'custom'
                              )),
    meeting_status               TEXT NOT NULL DEFAULT 'planned' CHECK (meeting_status IN (
                                     'planned', 'held', 'minutes_draft', 'minutes_approved', 'completed', 'cancelled'
                                 )),

    meeting_title                  TEXT NOT NULL,
    meeting_date                     DATE,
    meeting_location                   TEXT,
    chairperson_name                     TEXT,
    minute_taker_name                      TEXT,

    agenda_summary                           TEXT,
    minutes_summary                            TEXT,
    decisions_summary                            TEXT,
    next_meeting_date                              DATE,

    notes                                            TEXT,
    internal_notes                                     TEXT,
    settings                                             JSONB NOT NULL DEFAULT '{}',

    created_by                                             INTEGER,
    updated_by                                               INTEGER,
    created_at                                                TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                                  TIMESTAMPTZ DEFAULT NOW()
);

