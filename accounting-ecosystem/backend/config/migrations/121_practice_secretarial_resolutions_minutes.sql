CREATE INDEX IF NOT EXISTS idx_psm_client_id       ON practice_secretarial_meetings (client_id);
CREATE INDEX IF NOT EXISTS idx_psm_change_case_id  ON practice_secretarial_meetings (change_case_id) WHERE change_case_id IS NOT NULL;
