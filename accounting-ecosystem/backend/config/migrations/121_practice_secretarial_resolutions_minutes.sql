-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_secretarial_governance_events  (append-only audit log)
-- Never updated. Never deleted. No workflow status change without an event —
-- enforced at the application layer in secretarial-governance.js.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_secretarial_governance_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,

    source_type           TEXT NOT NULL CHECK (source_type IN ('resolution', 'meeting', 'attendee', 'decision')),
    source_id               INTEGER NOT NULL,   -- plain integer, no FK (Codebox 41 convention) — points into the table matching source_type
    client_id                 INTEGER,            -- plain integer, no FK — practice_clients (denormalized for fast per-client history queries, same pattern as practice_client_success_events)

    event_type                  TEXT NOT NULL CHECK (event_type IN (
                                    'resolution_created', 'resolution_updated', 'resolution_approved', 'resolution_signed',
                                    'resolution_implemented', 'resolution_cancelled',
                                    'meeting_created', 'meeting_updated', 'meeting_held', 'minutes_approved', 'meeting_cancelled',
                                    'attendee_added', 'attendee_updated',
                                    'decision_created', 'decision_updated', 'decision_approved', 'decision_implemented', 'decision_cancelled'
                                )),
    old_status                    TEXT,
    new_status                      TEXT,

    actor_user_id                     INTEGER,
    notes                               TEXT,
    metadata                             JSONB NOT NULL DEFAULT '{}',
    created_at                            TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_psge_company_id  ON practice_secretarial_governance_events (company_id);
CREATE INDEX IF NOT EXISTS idx_psge_source       ON practice_secretarial_governance_events (source_type, source_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_psge_client_id    ON practice_secretarial_governance_events (client_id) WHERE client_id IS NOT NULL;

COMMENT ON TABLE practice_secretarial_governance_events IS
    'Codebox 64 — Append-only log of resolution/meeting/attendee/decision activity. Never updated or deleted. Governance-significant events are ALSO pushed into practice_secretarial_events (Codebox 62''s Timeline) via secretarial.js''s writeSecretarialEvent() export — this table is not a second timeline, it is this module''s own detailed audit trail.';
