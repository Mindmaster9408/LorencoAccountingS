-- Migration 121: Secretarial Resolutions + Minutes Register Foundation
-- Codebox 64 — Lorenco Practice Management
--
-- NOT PDF generation. NOT e-signature. NOT CIPC submission. NOT document
-- storage. Structured governance record keeping only — future PDF/
-- e-signature must plug into these records, not replace them.
--
-- Codebox 63 controls the statutory CHANGE workflow (director appointments,
-- address changes, etc.). This codebox creates the governance DECISION
-- register behind those changes: who approved what, when, under which
-- authority, and which resolution/meeting supports it.
--
-- AUDIT (RULE A1 — see docs/new-app/64_secretarial_governance_register.md
-- Architect Freedom #1): confirmed no pre-existing tables with these names.
-- Note: practice_client_meetings (Codebox 61, Client Success) already exists
-- but is a DIFFERENT concept — relationship/communication meetings (quarterly
-- reviews, business reviews). practice_secretarial_meetings here is STATUTORY
-- governance meetings (directors meetings, AGMs) with attendees, minutes, and
-- resolutions — the two are never confused or merged; see docs Architect
-- Freedom #1 for the full distinction.
--
-- Tables created (all IF NOT EXISTS — safe to re-run):
--   practice_secretarial_resolutions
--   practice_secretarial_meetings
--   practice_secretarial_meeting_attendees
--   practice_secretarial_decisions
--   practice_secretarial_governance_events

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_secretarial_resolutions
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_secretarial_resolutions (
    id                      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id               INTEGER NOT NULL,
    client_id                  INTEGER NOT NULL,   -- plain integer, no FK — practice_clients (Codebox 41 convention)
    change_case_id               INTEGER,            -- plain integer, no FK — practice_secretarial_change_cases (Codebox 63). Nullable — not every resolution supports a statutory change.

    resolution_type                 TEXT NOT NULL CHECK (resolution_type IN (
                                        'directors_resolution', 'shareholders_resolution', 'written_resolution',
                                        'ordinary_resolution', 'special_resolution', 'trustee_resolution',
                                        'member_resolution', 'custom'
                                    )),
    resolution_status                 TEXT NOT NULL DEFAULT 'draft' CHECK (resolution_status IN (
                                          'draft', 'prepared', 'approved', 'signed', 'implemented', 'archived', 'cancelled'
                                      )),

    resolution_title                     TEXT NOT NULL,
    resolution_summary                     TEXT,
    resolution_date                          DATE,
    effective_date                             DATE,

    prepared_by                                  TEXT,   -- free text (person's name) — no separate person catalog exists, same convention as practice_company_directors.director_name
    approved_by                                    INTEGER,   -- plain integer — users.id (the staff member who actioned the approval in-app)
    approved_at                                      TIMESTAMPTZ,
    signed_at                                          TIMESTAMPTZ,
    implemented_at                                       TIMESTAMPTZ,

    reference_number                                       TEXT,
    notes                                                     TEXT,
    internal_notes                                             TEXT,

    -- A point-in-time copy of the resolution's key fields at signing — NOT a
    -- generated document (no PDF/e-signature here), just an auditable
    -- structured snapshot so later edits to notes/summary can never quietly
    -- rewrite what was actually signed off.
    content_snapshot                                             JSONB,
    settings                                                       JSONB NOT NULL DEFAULT '{}',

    created_by                                                     INTEGER,
    updated_by                                                       INTEGER,
    created_at                                                        TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                                          TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_psr_company_id        ON practice_secretarial_resolutions (company_id);
CREATE INDEX IF NOT EXISTS idx_psr_client_id          ON practice_secretarial_resolutions (client_id);
CREATE INDEX IF NOT EXISTS idx_psr_change_case_id     ON practice_secretarial_resolutions (change_case_id) WHERE change_case_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_psr_resolution_type    ON practice_secretarial_resolutions (company_id, resolution_type);
CREATE INDEX IF NOT EXISTS idx_psr_resolution_status  ON practice_secretarial_resolutions (company_id, resolution_status);
CREATE INDEX IF NOT EXISTS idx_psr_resolution_date    ON practice_secretarial_resolutions (company_id, resolution_date);

CREATE OR REPLACE FUNCTION fn_psr_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_psr_updated_at ON practice_secretarial_resolutions;
CREATE TRIGGER tg_psr_updated_at
    BEFORE UPDATE ON practice_secretarial_resolutions
    FOR EACH ROW EXECUTE FUNCTION fn_psr_updated_at();

COMMENT ON TABLE practice_secretarial_resolutions IS
    'Codebox 64 — Resolutions register. Optionally linked to a Codebox 63 statutory change case. NOT PDF generation, NOT e-signature — content_snapshot is a structured audit copy, not a generated/signed document.';

-- ─────────────────────────────────────────────────────────────────────────────
