    last_measured_at                                            TIMESTAMPTZ,
    notes                                                           TEXT,
    settings                                                           JSONB NOT NULL DEFAULT '{}',

    created_at                                                            TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                                               TIMESTAMPTZ DEFAULT NOW(),
    created_by                                                                  INTEGER,
    updated_by                                                                     INTEGER
