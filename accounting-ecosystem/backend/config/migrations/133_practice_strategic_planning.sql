-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_strategic_events  (append-only audit log)
-- One shared log for the whole module — nullable reference columns let a
-- single append-only table cover plan/objective/initiative/kpi_link/review
-- level events without five near-identical tables.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_strategic_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,

    plan_id                INTEGER,   -- plain integer, no FK. Nullable.
    objective_id              INTEGER,   -- plain integer, no FK. Nullable.
    initiative_id                INTEGER,   -- plain integer, no FK. Nullable.
    kpi_link_id                     INTEGER,   -- plain integer, no FK. Nullable.
    review_id                          INTEGER,   -- plain integer, no FK. Nullable.

    event_type                            TEXT NOT NULL CHECK (event_type IN (
                                              'plan_created', 'plan_updated', 'plan_activated', 'plan_completed', 'plan_archived', 'plan_cancelled',
                                              'objective_created', 'objective_updated', 'objective_status_changed', 'objective_cancelled',
                                              'initiative_created', 'initiative_updated', 'initiative_status_changed', 'initiative_cancelled',
                                              'kpi_link_created', 'kpi_link_updated', 'kpi_link_refreshed', 'kpi_link_cancelled',
                                              'review_created', 'review_updated', 'review_completed', 'review_action_required', 'review_cancelled'
                                          )),
    old_status                               TEXT,
    new_status                                  TEXT,

    actor_user_id                                  INTEGER,
    notes                                              TEXT,
    metadata                                              JSONB NOT NULL DEFAULT '{}',
    created_at                                               TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pse_company_id  ON practice_strategic_events (company_id);
CREATE INDEX IF NOT EXISTS idx_pse_plan_id      ON practice_strategic_events (plan_id) WHERE plan_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pse_objective_id  ON practice_strategic_events (objective_id) WHERE objective_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pse_initiative_id  ON practice_strategic_events (initiative_id) WHERE initiative_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pse_kpi_link_id      ON practice_strategic_events (kpi_link_id) WHERE kpi_link_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pse_review_id          ON practice_strategic_events (review_id) WHERE review_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pse_created_at          ON practice_strategic_events (created_at DESC);

COMMENT ON TABLE practice_strategic_events IS
    'Codebox 76 — Append-only log of every strategic plan/objective/initiative/KPI-link/review lifecycle event. Never updated or deleted. Note: plan_cancelled, objective_cancelled, initiative_cancelled, and kpi_link_cancelled extend the spec''s literal event list to cover this module''s soft-cancel DELETE endpoints — every status change must have a matching event, including cancellation.';

-- Note on event_type list: the spec's literal list of 17 event types did not
-- include plan_cancelled/objective_cancelled/initiative_cancelled/
-- kpi_link_cancelled, but this module's DELETE endpoints soft-cancel rather
