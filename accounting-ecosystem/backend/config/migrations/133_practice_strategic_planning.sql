CREATE TABLE IF NOT EXISTS practice_strategic_plans (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,

    plan_name              TEXT NOT NULL,
    plan_year                 INTEGER NOT NULL,
    period_start                 DATE NOT NULL,
    period_end                      DATE NOT NULL,

    plan_status                        TEXT NOT NULL DEFAULT 'draft' CHECK (plan_status IN (
                                           'draft', 'active', 'under_review', 'completed', 'archived', 'cancelled'
                                       )),

    vision_statement                       TEXT,
    strategic_theme                           TEXT,
    executive_summary                            TEXT,
    partner_notes                                   TEXT,
    cancellation_reason                                TEXT,   -- Architect Freedom addition — required by DELETE (soft-cancel), same "reason required" convention as every other consequential action this session.
    settings                                              JSONB NOT NULL DEFAULT '{}',

    created_at                                               TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                                  TIMESTAMPTZ DEFAULT NOW(),
    created_by                                                     INTEGER,
    updated_by                                                        INTEGER
);

CREATE INDEX IF NOT EXISTS idx_psp_company_id  ON practice_strategic_plans (company_id);
CREATE INDEX IF NOT EXISTS idx_psp_status       ON practice_strategic_plans (company_id, plan_status);
CREATE INDEX IF NOT EXISTS idx_psp_year          ON practice_strategic_plans (company_id, plan_year);

CREATE OR REPLACE FUNCTION fn_psp_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_psp_updated_at ON practice_strategic_plans;
CREATE TRIGGER tg_psp_updated_at
    BEFORE UPDATE ON practice_strategic_plans
    FOR EACH ROW EXECUTE FUNCTION fn_psp_updated_at();

COMMENT ON TABLE practice_strategic_plans IS
    'Codebox 76 — Annual/period strategic plan. draft/active/under_review/completed/archived/cancelled. Objectives, initiatives, and reviews all reference a plan.';

