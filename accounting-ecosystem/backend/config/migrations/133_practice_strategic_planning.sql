CREATE TABLE IF NOT EXISTS practice_strategic_kpi_links (
    id                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id             INTEGER NOT NULL,
    objective_id              INTEGER NOT NULL,   -- plain integer, no FK — practice_strategic_objectives (Codebox 41 convention)

    kpi_source                   TEXT NOT NULL CHECK (kpi_source IN (
                                     'management_dashboard', 'kpi_history', 'partner_scorecard', 'profitability',
                                     'client_success', 'risk', 'qms', 'planning', 'capacity', 'tax', 'secretarial', 'custom'
                                 )),
    metric_key                       TEXT NOT NULL,
    metric_label                        TEXT NOT NULL,

    baseline_value                          NUMERIC(14, 2),
    target_value                               NUMERIC(14, 2),
    current_value                                 NUMERIC(14, 2),

    direction                                        TEXT NOT NULL CHECK (direction IN ('increase', 'decrease', 'maintain', 'threshold')),
