-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_pilot_readiness_runs
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_pilot_readiness_runs (
    id                        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id                INTEGER NOT NULL,

    run_name                  TEXT NOT NULL,
    run_type                  TEXT NOT NULL CHECK (run_type IN (
                                  'internal_test', 'pilot_test', 'pre_launch', 'go_no_go', 'post_fix_review'
                              )),

    readiness_status          TEXT NOT NULL DEFAULT 'not_ready' CHECK (readiness_status IN (
                                  'not_ready', 'needs_attention', 'pilot_ready', 'launch_ready', 'blocked'
                              )),

    overall_score             NUMERIC(5, 2),
    critical_blockers         INTEGER NOT NULL DEFAULT 0,
    high_issues               INTEGER NOT NULL DEFAULT 0,
    medium_issues             INTEGER NOT NULL DEFAULT 0,
    low_issues                INTEGER NOT NULL DEFAULT 0,

    operational_health_run_id INTEGER,   -- plain integer, no FK — practice_health_check_runs (Codebox 41 convention). Nullable — a run may find none exists yet.

    module_matrix             JSONB NOT NULL DEFAULT '{}',
    smoke_test_summary        JSONB NOT NULL DEFAULT '{}',
    navigation_summary        JSONB NOT NULL DEFAULT '{}',
    known_issue_summary       JSONB NOT NULL DEFAULT '{}',
    readiness_snapshot        JSONB NOT NULL DEFAULT '{}',

    decision                  TEXT NOT NULL DEFAULT 'no_decision' CHECK (decision IN ('no_decision', 'go', 'no_go', 'conditional_go')),
    decision_notes            TEXT,
    decided_by                INTEGER,
    decided_at                TIMESTAMPTZ,

    created_at                TIMESTAMPTZ DEFAULT NOW(),
    created_by                INTEGER
);

CREATE INDEX IF NOT EXISTS idx_pprr_company_id ON practice_pilot_readiness_runs (company_id);
CREATE INDEX IF NOT EXISTS idx_pprr_status ON practice_pilot_readiness_runs (company_id, readiness_status);
CREATE INDEX IF NOT EXISTS idx_pprr_created_at ON practice_pilot_readiness_runs (created_at DESC);

COMMENT ON TABLE practice_pilot_readiness_runs IS
    'Codebox 80 — One snapshot per readiness run. Reads the latest Operational Health run + automation/role-link/known-issue/checklist state at run time; never invents health data.';

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_pilot_checklist_items
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_pilot_checklist_items (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         INTEGER NOT NULL,

    check_category     TEXT NOT NULL CHECK (check_category IN (
                           'navigation', 'access_control', 'core_operations', 'clients', 'planning',
                           'secretarial', 'tax', 'engagement', 'reporting', 'automation', 'health',
                           'data', 'security', 'documentation', 'pilot_admin', 'custom'
                       )),

    check_title        TEXT NOT NULL,
    check_description  TEXT,

    check_status       TEXT NOT NULL DEFAULT 'not_started' CHECK (check_status IN (
                           'not_started', 'passed', 'failed', 'warning', 'not_applicable', 'deferred'
                       )),
    severity           TEXT NOT NULL DEFAULT 'medium' CHECK (severity IN ('critical', 'high', 'medium', 'low', 'info')),

    linked_module      TEXT,
    linked_url         TEXT,
    owner_team_member_id INTEGER,   -- plain integer, no FK — practice_team_members. Nullable.
    due_date           DATE,

    evidence_notes     TEXT,
    resolution_notes   TEXT,
    sort_order         INTEGER NOT NULL DEFAULT 0,
    is_default         BOOLEAN NOT NULL DEFAULT FALSE,

    created_at         TIMESTAMPTZ DEFAULT NOW(),
    updated_at         TIMESTAMPTZ DEFAULT NOW(),
    created_by         INTEGER,
    updated_by         INTEGER
);

CREATE INDEX IF NOT EXISTS idx_ppci_company_id ON practice_pilot_checklist_items (company_id);
CREATE INDEX IF NOT EXISTS idx_ppci_status ON practice_pilot_checklist_items (company_id, check_status);
CREATE INDEX IF NOT EXISTS idx_ppci_category ON practice_pilot_checklist_items (company_id, check_category);
CREATE INDEX IF NOT EXISTS idx_ppci_sort ON practice_pilot_checklist_items (company_id, sort_order);

CREATE OR REPLACE FUNCTION fn_ppci_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_ppci_updated_at ON practice_pilot_checklist_items;
CREATE TRIGGER tg_ppci_updated_at
    BEFORE UPDATE ON practice_pilot_checklist_items
    FOR EACH ROW EXECUTE FUNCTION fn_ppci_updated_at();

COMMENT ON TABLE practice_pilot_checklist_items IS
    'Codebox 80 — Manager-editable launch checklist, seeded with 15 default smoke-test items covering login/access/core pages/reporting/automation/health/navigation. is_default marks seeded rows so re-seeding never duplicates them.';

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_pilot_known_issues
