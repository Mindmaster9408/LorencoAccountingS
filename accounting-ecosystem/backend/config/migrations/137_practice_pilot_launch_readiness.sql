--   this module does NOT call it fresh. Per the spec's own instruction
--   ("Use: Operational Health latest run... Do not invent health data"),
--   pilot-readiness.js reads the latest STORED practice_health_check_runs
--   row only. If none exists, readiness_status is forced to at least
--   'needs_attention' and a warning code OPERATIONAL_HEALTH_NOT_RUN is
--   raised — exactly as specified, never a fabricated score.
--
--   practice_automation_runs (Codebox 78) is read directly (same
--   count-only pattern management-dashboard.js and operational-health.js
--   both already use) for recent failed-run counts — never re-derives
--   automation.js's own run-status logic.
--
--   lib/team-access.js's role concept is reused, not reimplemented: this
--   migration adds no new role/permission table, and the one new frontend-
--   facing endpoint this codebox adds (GET /api/practice/team/me) calls
--   teamAccess.getMyTeamMember()/isManager() directly — it exists only so
--   the frontend can safely INFER whether to show manager-level nav
--   groups; the backend remains the sole authorization boundary
--   unchanged (every existing manager-gated route is untouched).
--
--   Navigation consolidation (layout.js) is a pure frontend/JS change —
--   no new table backs it; navigation_summary on a readiness run is a
--   static, honest description of the shipped grouped-nav structure
--   (group count, page count), not a runtime health probe, since
--   navigation structure is code, not queryable data.
--
