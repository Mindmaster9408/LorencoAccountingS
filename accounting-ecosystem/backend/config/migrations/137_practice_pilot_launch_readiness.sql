                           )),

    severity               TEXT NOT NULL DEFAULT 'medium' CHECK (severity IN ('critical', 'high', 'medium', 'low')),
    issue_status           TEXT NOT NULL DEFAULT 'open' CHECK (issue_status IN (
                               'open', 'in_progress', 'resolved', 'accepted_risk', 'deferred', 'cancelled'
                           )),

    affected_module        TEXT,
    affected_url           TEXT,
    reproduction_steps     TEXT,
    expected_result        TEXT,
    actual_result           TEXT,
    workaround             TEXT,
    resolution_notes       TEXT,

    reported_by            INTEGER,
    assigned_team_member_id INTEGER,   -- plain integer, no FK — practice_team_members. Nullable.
    resolved_by            INTEGER,
    resolved_at            TIMESTAMPTZ,

    created_at             TIMESTAMPTZ DEFAULT NOW(),
    updated_at             TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ppki_company_id ON practice_pilot_known_issues (company_id);
CREATE INDEX IF NOT EXISTS idx_ppki_status ON practice_pilot_known_issues (company_id, issue_status);
CREATE INDEX IF NOT EXISTS idx_ppki_severity ON practice_pilot_known_issues (company_id, severity);
CREATE INDEX IF NOT EXISTS idx_ppki_category ON practice_pilot_known_issues (company_id, issue_category);

CREATE OR REPLACE FUNCTION fn_ppki_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_ppki_updated_at ON practice_pilot_known_issues;
CREATE TRIGGER tg_ppki_updated_at
    BEFORE UPDATE ON practice_pilot_known_issues
    FOR EACH ROW EXECUTE FUNCTION fn_ppki_updated_at();

COMMENT ON TABLE practice_pilot_known_issues IS
    'Codebox 80 — Known-issues register for pilot launch. A critical severity issue in the security/access category forces readiness_status to blocked regardless of overall score.';

-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_pilot_events  (append-only audit log)
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_pilot_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         INTEGER NOT NULL,

    readiness_run_id   INTEGER,   -- plain integer, no FK. Nullable.
    checklist_item_id  INTEGER,   -- plain integer, no FK. Nullable.
    known_issue_id     INTEGER,   -- plain integer, no FK. Nullable.

    event_type         TEXT NOT NULL CHECK (event_type IN (
                           'readiness_run_created', 'readiness_run_completed', 'checklist_seeded',
                           'checklist_item_updated', 'known_issue_created', 'known_issue_updated',
                           'known_issue_resolved', 'go_decision_recorded', 'navigation_refactor_completed',
                           'smoke_test_recorded'
                       )),
    old_status         TEXT,
    new_status         TEXT,

    actor_user_id      INTEGER,
    notes              TEXT,
    metadata           JSONB NOT NULL DEFAULT '{}',
    created_at         TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ppe_company_id ON practice_pilot_events (company_id);
CREATE INDEX IF NOT EXISTS idx_ppe_run_id ON practice_pilot_events (readiness_run_id) WHERE readiness_run_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_ppe_checklist_id ON practice_pilot_events (checklist_item_id) WHERE checklist_item_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_ppe_issue_id ON practice_pilot_events (known_issue_id) WHERE known_issue_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_ppe_created_at ON practice_pilot_events (created_at DESC);

COMMENT ON TABLE practice_pilot_events IS
    'Codebox 80 — Append-only log of every readiness run, checklist change, known-issue change, and Go/No-Go decision. Never updated or deleted.';
