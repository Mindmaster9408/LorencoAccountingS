-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_executive_events  (append-only audit log)
-- One shared log for the whole module — nullable reference columns let a
-- single append-only table cover report/decision/action level events
-- without three near-identical tables (same convention as Codebox 76's
-- practice_strategic_events).
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_executive_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,

    report_id              INTEGER,   -- plain integer, no FK. Nullable.
    decision_id               INTEGER,   -- plain integer, no FK. Nullable.
    action_id                    INTEGER,   -- plain integer, no FK. Nullable.

    event_type                       TEXT NOT NULL CHECK (event_type IN (
                                         'report_created', 'report_generated', 'report_updated', 'report_reviewed',
                                         'report_approved', 'report_published', 'report_archived', 'report_cancelled',
                                         'decision_created', 'decision_updated', 'decision_completed', 'decision_cancelled',
                                         'action_created', 'action_updated', 'action_completed', 'action_cancelled'
                                     )),
    old_status                          TEXT,
    new_status                             TEXT,

    actor_user_id                             INTEGER,
    notes                                         TEXT,
    metadata                                        JSONB NOT NULL DEFAULT '{}',
    created_at                                         TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pee_company_id  ON practice_executive_events (company_id);
CREATE INDEX IF NOT EXISTS idx_pee_report_id     ON practice_executive_events (report_id) WHERE report_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pee_decision_id     ON practice_executive_events (decision_id) WHERE decision_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pee_action_id          ON practice_executive_events (action_id) WHERE action_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pee_created_at            ON practice_executive_events (created_at DESC);

COMMENT ON TABLE practice_executive_events IS
    'Codebox 77 — Append-only log of every executive report/decision/action lifecycle event. Never updated or deleted. Note: report_cancelled/decision_cancelled/action_cancelled and decision_updated/action_updated extend the spec''s literal 12-event list to cover this module''s soft-cancel DELETE endpoints and plain field edits — every status change must have a matching event, including cancellation and edits, same convention as Codebox 76.';
