-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_executive_reports
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_executive_reports (
    id                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id            INTEGER NOT NULL,

    report_title          TEXT NOT NULL,
    report_type           TEXT NOT NULL CHECK (report_type IN (
                               'monthly', 'quarterly', 'annual', 'board', 'management', 'custom'
                           )),

    period_start          DATE NOT NULL,
    period_end            DATE NOT NULL,
    period_key            TEXT,

    report_status         TEXT NOT NULL DEFAULT 'draft' CHECK (report_status IN (
                               'draft', 'generated', 'under_review', 'approved', 'published', 'archived', 'cancelled'
                           )),

    executive_summary        TEXT,
    practice_health_summary     TEXT,
    key_wins                      TEXT,
    key_concerns                     TEXT,
    key_decisions                       TEXT,
    recommendations                        TEXT,
    cancellation_reason                       TEXT,   -- Architect Freedom addition — required by DELETE (soft-cancel), same "reason required" convention as every other consequential action this session.

    -- The frozen board-pack snapshot — the single source of truth for
    -- report-data/report-html/report-pdf. Never recalculated after
    -- generation; buildExecutiveReport() is called again only via an
    -- explicit regenerate action, which overwrites this column deliberately.
    report_snapshot           JSONB NOT NULL DEFAULT '{}',
    settings                     JSONB NOT NULL DEFAULT '{}',

    generated_by                   INTEGER,   -- plain integer, no FK — users.id
    generated_at                      TIMESTAMPTZ,
    approved_by                          INTEGER,   -- plain integer, no FK — users.id
    approved_at                             TIMESTAMPTZ,
    published_by                               INTEGER,   -- plain integer, no FK — users.id
    published_at                                  TIMESTAMPTZ,

    created_at                                       TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                           TIMESTAMPTZ DEFAULT NOW(),
    created_by                                              INTEGER,
    updated_by                                                 INTEGER
);

CREATE INDEX IF NOT EXISTS idx_per_company_id  ON practice_executive_reports (company_id);
CREATE INDEX IF NOT EXISTS idx_per_status       ON practice_executive_reports (company_id, report_status);
CREATE INDEX IF NOT EXISTS idx_per_type          ON practice_executive_reports (company_id, report_type);
CREATE INDEX IF NOT EXISTS idx_per_period         ON practice_executive_reports (company_id, period_start, period_end);

CREATE OR REPLACE FUNCTION fn_per_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_per_updated_at ON practice_executive_reports;
CREATE TRIGGER tg_per_updated_at
    BEFORE UPDATE ON practice_executive_reports
    FOR EACH ROW EXECUTE FUNCTION fn_per_updated_at();

COMMENT ON TABLE practice_executive_reports IS
    'Codebox 77 — Executive board pack for a reporting period. draft/generated/under_review/approved/published/archived/cancelled. report_snapshot is frozen at generation time and never silently recalculated.';

