-- TABLE: practice_executive_report_sections
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_executive_report_sections (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,
    report_id              INTEGER NOT NULL,   -- plain integer, no FK — practice_executive_reports (Codebox 41 convention)

    section_key                TEXT NOT NULL CHECK (section_key IN (
                                   'executive_summary', 'practice_health', 'strategy', 'kpis', 'partner_scorecards',
                                   'profitability', 'pricing', 'client_success', 'capacity', 'planning', 'risk',
                                   'quality', 'secretarial', 'notifications', 'learning', 'recommendations',
                                   'decisions', 'actions'
                               )),
    section_title                  TEXT NOT NULL,
    section_order                     INTEGER NOT NULL DEFAULT 0,

    section_status                       TEXT NOT NULL DEFAULT 'included' CHECK (section_status IN (
                                             'included', 'hidden', 'manual'
                                         )),

    -- Frozen at generation time for every engine-sourced section
    -- (section_status <> 'manual'); NULL for a manual section a partner adds
    -- freeform. Never recalculated except by a full report regenerate.
    section_snapshot                        JSONB,
    notes                                       TEXT,

    created_at                                     TIMESTAMPTZ DEFAULT NOW(),
    updated_at                                        TIMESTAMPTZ DEFAULT NOW(),
    created_by                                           INTEGER,
    updated_by                                              INTEGER
);

CREATE INDEX IF NOT EXISTS idx_pers_company_id  ON practice_executive_report_sections (company_id);
CREATE INDEX IF NOT EXISTS idx_pers_report_id     ON practice_executive_report_sections (report_id);
CREATE INDEX IF NOT EXISTS idx_pers_order          ON practice_executive_report_sections (report_id, section_order);

-- One row per section per report — generation is idempotent per section_key,
-- never silently duplicating rows on regenerate (upsert on this constraint).
CREATE UNIQUE INDEX IF NOT EXISTS uq_pers_report_section
    ON practice_executive_report_sections (report_id, section_key);

CREATE OR REPLACE FUNCTION fn_pers_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tg_pers_updated_at ON practice_executive_report_sections;
CREATE TRIGGER tg_pers_updated_at
    BEFORE UPDATE ON practice_executive_report_sections
    FOR EACH ROW EXECUTE FUNCTION fn_pers_updated_at();

COMMENT ON TABLE practice_executive_report_sections IS
    'Codebox 77 — One reusable section within an executive report. included/hidden/manual. section_snapshot is frozen per engine-sourced section; manual sections carry no snapshot and are partner-authored via notes.';

