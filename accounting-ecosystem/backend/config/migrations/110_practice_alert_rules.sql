-- NOT AI. NOT automatic threshold tuning. NOT machine learning.
-- Deterministic, database-driven alert thresholds only.
--
-- Tables created (all IF NOT EXISTS — safe to re-run):
--   practice_alert_rule_groups
--   practice_alert_rules
