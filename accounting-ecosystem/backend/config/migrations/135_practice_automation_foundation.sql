CREATE TABLE IF NOT EXISTS practice_automation_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id         INTEGER NOT NULL,

    rule_id            INTEGER,   -- plain integer, no FK. Nullable.
    run_id             INTEGER,   -- plain integer, no FK. Nullable.

    event_type         TEXT NOT NULL CHECK (event_type IN (
                           'rule_created', 'rule_updated', 'rule_activated', 'rule_paused', 'rule_disabled',
                           'rule_approved', 'rule_archived', 'rule_cancelled',
                           'run_started', 'run_completed', 'run_failed', 'run_cancelled', 'dry_run_completed',
                           'action_executed', 'action_skipped'
                       )),
    old_status         TEXT,
    new_status         TEXT,

    actor_user_id      INTEGER,
    notes              TEXT,
    metadata           JSONB NOT NULL DEFAULT '{}',
    created_at         TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pae_company_id ON practice_automation_events (company_id);
CREATE INDEX IF NOT EXISTS idx_pae_rule_id ON practice_automation_events (rule_id) WHERE rule_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pae_run_id ON practice_automation_events (run_id) WHERE run_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_pae_created_at ON practice_automation_events (created_at DESC);

COMMENT ON TABLE practice_automation_events IS
    'Codebox 78 — Append-only log of every rule lifecycle change and every run''s lifecycle. Never updated or deleted. Note: rule_cancelled extends the spec''s literal event list to cover this module''s soft-cancel DELETE endpoint, same convention as Codeboxes 76/77.';
