-- ─────────────────────────────────────────────────────────────────────────────
-- TABLE: practice_entity_lifecycle_events  (append-only audit log)
-- Never updated. Never deleted. No lifecycle status change and no transition
-- completion without a matching event — enforced at the application layer.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS practice_entity_lifecycle_events (
    id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id          INTEGER NOT NULL,
    client_id             INTEGER,   -- plain integer, no FK — practice_clients (denormalized for fast per-client history queries)

    source_type             TEXT NOT NULL CHECK (source_type IN ('lifecycle_profile', 'lifecycle_transition', 'checklist_item')),
    source_id                 INTEGER NOT NULL,   -- plain integer, no FK (Codebox 41 convention)

    event_type                  TEXT NOT NULL CHECK (event_type IN (
                                    'lifecycle_profile_created', 'lifecycle_profile_updated',
                                    'transition_created', 'transition_updated',
                                    'checklist_generated', 'checklist_item_updated',
                                    'transition_submitted_review', 'transition_approved', 'transition_rejected',
                                    'transition_implemented', 'transition_completed', 'transition_cancelled',
                                    'lifecycle_status_changed', 'lifecycle_reviewed'
                                )),
    old_status                    TEXT,
    new_status                      TEXT,

    actor_user_id                      INTEGER,
    notes                                TEXT,
    metadata                              JSONB NOT NULL DEFAULT '{}',
    created_at                              TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_pele_company_id  ON practice_entity_lifecycle_events (company_id);
CREATE INDEX IF NOT EXISTS idx_pele_source       ON practice_entity_lifecycle_events (source_type, source_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_pele_client_id    ON practice_entity_lifecycle_events (client_id) WHERE client_id IS NOT NULL;

COMMENT ON TABLE practice_entity_lifecycle_events IS
    'Codebox 68 — Append-only log of lifecycle profile/transition/checklist activity. Never updated or deleted.';
